package com.jajahome.model;

/**
 *  获取手机验证码 model
 */
public class AuthCodeModle {

    /**
     * time : 300
     * rnt : 20160719102103,0
     */

    private DataBean data;
    /**
     * data : {"time":300,"rnt":"20160719102103,0"}
     * cmd : sms_request
     * status : 0
     */

    private String cmd;
    private int status;

    public DataBean getData() {
        return data;
    }

    public void setData(DataBean data) {
        this.data = data;
    }

    public String getCmd() {
        return cmd;
    }

    public void setCmd(String cmd) {
        this.cmd = cmd;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public static class DataBean {
        private int time;
        private String rnt;

        public int getTime() {
            return time;
        }

        public void setTime(int time) {
            this.time = time;
        }

        public String getRnt() {
            return rnt;
        }

        public void setRnt(String rnt) {
            this.rnt = rnt;
        }
    }
}
